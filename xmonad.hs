main :: IO ()
main = do
    xmproc <- spawnPipe "xmobar -x /home/lf01/.config/xmobar/.xmobarrc"
    xmonad $ ewmh def
        { manageHook         = myManageHook <+> manageDocks
        , handleEventHook    = docksEventHook <+> fullscreenEventHook
        , modMask            = myModMask
        , terminal           = myTerminal
        , startupHook        = myStartupHook
        , layoutHook         = showWName' myShowWNameTheme $ myLayoutHook
        , workspaces         = myWorkspaces
        , borderWidth        = myBorderWidth
        , normalBorderColor  = myNormColor
        , focusedBorderColor = myFocusColor
        , logHook = dynamicLogWithPP $ namedScratchpadFilterOutWorkspacePP $ xmobarPP
              { ppOutput = \x -> hPutStrLn xmproc x
              , ppCurrent = xmobarColor "#c792ea" "" . wrap "<box type=Bottom width=2 color=#c792ea>" "</box>"
              , ppVisible = xmobarColor "#c792ea" "" -- . clickable
              , ppHidden = xmobarColor "#82AAFF" "" . wrap "<box type=Top width=2 color=#82AAFF>" "</box>" -- . clickable
              , ppHiddenNoWindows = xmobarColor "#82AAFF" "" -- . clickable
              , ppTitle = xmobarColor "#E5E5E5" "" . shorten 60 
              , ppSep =  "<fc=#E5E5E5> <fn=1>|</fn> </fc>" 
              , ppUrgent = xmobarColor "#C45500" "" . wrap "!" "!" 
              , ppExtras  = [windowCount] 
              , ppOrder  = \(ws:l:t:ex) -> [ws,l]++ex++[t]
              }
        } --`additionalKeysP` myKeys

